package game;

/**
 * The interface Game.
 */
public interface Game {
    /**
     * Starts the game.
     */
    void start();

    /**
     * Resolve the game.
     */
    void resolve();

    /**
     * Handles the end.
     */
    void end();

    /**
     * Resets the game.
     */
    void reset();
}
